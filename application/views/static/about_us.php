<h3>Ryan Zintgraff, President</h3>
</p>
<p>What began with an interest in the history, artwork and stories of
stamps as a young boy has transformed into what is now KLECT. The idea
for KLECT presented itself when Zintgraff was writing code for an online
inventory system for his own personal stamp collection. He ultimately
wanted to be able to share and trade with other collectors. After three
years of preliminary research and development, KLECT went live.</p>
<p>Zintgraff received a Bachelors of Business Administration in
Marketing from Texas Tech University in Lubbock, Texas and a Masters of
Business Administration from St. Edwards University in Austin, Texas.
His professional experience is centered around marketing and sales
within the information technology industry. Among the positions
Zintgraff has held are Vice President of Sales and Marketing, Marketing
Account Executive and World Wide Professional Services Manager. However,
do not be fooled by his many successes in large established companies
such Xerox, Pervasive Software, AT&amp;T, and ePartners. Zintgraff is no
stranger to the start-up environment. He has pioneered multiple
entrepreneurial successes in addition to KLECT.</p>
<p>Zintgraff also continues to serve in the US Navy as a Supply and
Logistics Officer, but he does find time to enjoy himself. When he is
not spending time with his family or tinkering with KLECT, Zintrgraff is
dominating the handball court, leading a group of 110 Cub Scouts as a
Cubmaster or restoring a 1953 Chevy Bel-Air.</p>
